// Operadores Relacionales ( Comparación)
let a = 5;
let b = '5';
console.log(a);
console.log(b);

// Operador igualdad ==
//solo compara valores, y hace una conversión si es necesario
console.log("a == b -> ", a == b);
// String interporlation
console.log(`${a} == ${b} -> ${a == b}`);


//Operador igualdad estricta o exacta ===
//se compara el valor y el tipo de dato
console.log("a === b -> ", a === b);
// String interpolation
console.log(`${a} === ${b} -> ${a === b}`);


// Operador distinto !=
// Compara el valor y convierte el tipo si es necesario
console.log(`${a} != ${b} -> ${a != b}`);

// Operador distinto exacto !==
//Compara el valor y el tipo de dato
console.log(`${a} !== ${b} -> ${a !== b}`);

// Operador menor que <
console.log(`${a} < ${b} -> ${a < b}`);
// Operador menor o igual que <=
console.log(`${a} <= ${b} -> ${a <= b}`);
// Operador mayor que >
console.log(`${a} > ${b} -> ${a > b}`);
// Operador mayor o igual que >=
console.log(`${a} >= ${b} -> ${a >= b}`);


