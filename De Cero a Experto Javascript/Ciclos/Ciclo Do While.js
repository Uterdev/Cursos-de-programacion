/* El ciclo do while ejecuta al menos una vez su bloque 
de código debido a que la condición a revisar 
se encuentra al final del bloque.
*/
// Ciclo Do While
// Imprimir los valores del 0 al 100
let contador = 90, repeticiones = 100;
do{
   console.log(contador);
   contador++ 
}while(contador <= repeticiones);
